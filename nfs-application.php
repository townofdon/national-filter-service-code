    <h3>
    ARTICLE 23-A<br/>
    LICENSURE AND EMPLOYMENT OF PERSONS PREVIOUSLY CONVICTED OF ONE OR MORE CRIMINAL OFFENSES
    </h3>

    <p>
    <strong>Section 750. Definitions.</strong>
    </p>

    <p>
    <strong>Section 751. Applicability.</strong>
    </p>

    <p>
    <strong>Section 752. Unfair discrimination against persons previously convicted of one or more criminal offenses prohibited.</strong>
    </p>

    <p>
    <strong>Section 753. Factors to be considered concerning a previous criminal conviction; presumption.</strong>
    </p>

    <p>
    <strong>Section 754. Written statement upon denial of license or employment.</strong>
    </p>

    <p>
    <strong>Section 755. Enforcement.</strong>
    </p>

    <p>
    <strong>§750. Definitions.</strong><br/>
    For the purposes of this article, the following terms shall have the following meanings:<br/>
    (1) "Public agency" means the state or any local subdivision thereof, or any state or local department, agency, board or
    commission.<br/>
    (2) "Private employer" means any person, company, corporation, labor organization or association which employs ten or more persons.<br/>
    (3) "Direct relationship" means that the nature of criminal conduct for which the person was convicted has a direct bearing on his fitness or 
    ability to perform one or more of the duties or responsibilities necessarily related to the license, opportunity, or job in question.<br/>
    (4) "License" means any certificate, license, permit or grant of permission required by the laws of this state, its political
    subdivisions or instrumentalities as a condition for the lawful practice of any occupation, employment, trade, vocation,
    business, or profession. Provided, however, that "license" shall not, for the purposes of this article, include any license or
    permit to own, possess, carry, or fire any explosive, pistol, handgun, rifle, shotgun, or other firearm.<br/>
    (5) "Employment" means any occupation, vocation or employment, or any form of vocational or educational training. Provided, however, that 
    "employment" shall not, for the purposes of this article, include membership in any law enforcement agency.
    </p>

    <p>
    <strong>§751. Applicability.</strong><br/>
    The provisions of this article shall apply to any application by any person for a license or employment at any public or private employer, 
    who has previously been convicted of one or more criminal offenses in this state or in any other jurisdiction, and to any license or 
    employment held by any person whose conviction of one or more criminal offenses in this state or in any other jurisdiction preceded 
    such employment or granting of a license, except where a mandatory forfeiture, disability or bar to employment is imposed by law, 
    and has not been removed by an executive pardon, certificate of relief from disabilities or certificate of good conduct. Nothing 
    in this article shall be construed to affect any right an employer may have with respect to an intentional misrepresentation in 
    connection with an application for employment made by a prospective employee or previously made by a current employee.
    </p>

    <p>
    <strong>§752. Unfair discrimination against persons previously convicted of one or more criminal offenses prohibited.</strong><br/>
    No application for any license or employment, and no employment or license held by an individual, to which the provisions of this article are applicable, shall be denied or acted upon adversely by reason of the individual's having been previously
    convicted of one or more criminal offenses, or by reason of a finding of lack of "good moral character" when such finding is
    based upon the fact that the individual has previously been convicted of one or more criminal offenses, unless:<br/>
    (1) There is a direct relationship between one or more of the previous criminal offenses and the specific license or
    employment sought or held by the individual; or<br/>
    (2) the issuance or continuation of the license or the granting or continuation of the employment would involve an
    unreasonable risk to property or to the safety or welfare of specific individuals or the general public.
    </p>

    <p>
    <strong>§753. Factors to be considered concerning a previous criminal conviction; presumption.</strong><br/>
    1. In making a determination pursuant to section seven hundred fifty-two of this chapter, the public agency or
    private employer shall consider the following factors:<br/>
    (a) The public policy of this state, as expressed in this act, to encourage the licensure and employment of persons
    previously convicted of one or more criminal offenses.<br/>
    (b) The specific duties and responsibilities necessarily related to the license or employment sought or held by the
    person.<br/>
    (c) The bearing, if any, the criminal offense or offenses for which the person was previously convicted will have on his
    fitness or ability to perform one or more such duties or responsibilities.<br/>
    (d) The time which has elapsed since the occurrence of the criminal offense or offenses.<br/>
    (e) The age of the person at the time of occurrence of the criminal offense or offenses.<br/>
    (f) The seriousness of the offense or offenses.<br/>
    (g) Any information produced by the person, or produced on his behalf, in regard to his rehabilitation and good
    conduct.<br/>
    (h) The legitimate interest of the public agency or private employer in protecting property, and the safety and welfare
    of specific individuals or the general public.<br/>
    2. In making a determination pursuant to section seven hundred fifty-two of this chapter, the public agency or private employer 
    shall also give consideration to a certificate of relief from disabilities or a certificate of good conduct issued to the applicant, 
    which certificate shall create a presumption of rehabilitation in regard to the offense or offenses specified therein.
    </p>

    <p>
